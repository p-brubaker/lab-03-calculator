export function add(x, y) {
    return x + y;
}

export function subtract(x, y) {
    return x - y;
}

export function multiply(x, y) {
    return x * y;
}

export function divide(x, y) {
    return x / y;
}

export function mod(x, y) {
    return x % y;
}

export function intdiv(x, y) {
    return Math.floor(x / y);
}
